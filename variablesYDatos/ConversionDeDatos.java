package variablesYDatos;

public class ConversionDeDatos {
    public static void main(String[] args) {

        String numeroStr = "50";
        System.out.println("Número String: " + numeroStr + "\n");

        //Convierto el string a integer
        int numeroInt = Integer.parseInt(numeroStr);
        System.out.println("Número String: " + numeroInt + "\n");
    }
}
